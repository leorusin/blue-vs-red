/** casualties can never exceed the country's population */
export function cappedCasualties(raw: number, country: Country): number {
  return Math.min(raw, country.population);
}

export const GAME_VERSIONS = ['0.1.0', '1.0.1'] as const;
export type GameVersion = (typeof GAME_VERSIONS)[number];

/** the 16 countries of v0.1.0, in their original menu order */
